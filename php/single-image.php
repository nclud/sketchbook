    <h1>Sketchbook Sessions: A Creative Speaker Series</h1>
    <span class="date-time">February 26, 2014</span>
</header>

<article id="post-content" class="padded-horizontal-large">
	<p>We have a rich history of knowledge sharing at nclud – our company is built on the value of fostering community. Our closest colleagues &amp; clients got to know us by spending time in our studio and talking with us. Delivering great content &amp; events has been such a rewarding part of our culture that we’re giving it a name and clearer purpose: <em>Sketchbook Sessions</em>.</p>

	<p><em>Sketchbook Sessions</em> will continue our tradition of producing free &amp; open talks that have included speakers like <a href='https://www.facebook.com/media/set/?set=a.10151426098216103.553192.12294301102' target='_blank'>Aaron Draplin</a>, <a href='https://www.facebook.com/nclud/posts/10151655341761103' target='_blank'>Ian MacKaye</a>, <a href='https://www.facebook.com/media/set/?set=a.10152044644821103.1073741844.12294301102' target='_blank'>Jon Newman</a>, <a href='https://www.facebook.com/nclud/posts/10151880627066103' target='_blank'>El Jefe</a>, &amp; others – yet also hosting inspiring discussions &amp; in-depth presentations by our team members.</p>

	<p>As we further the conversation on the convergence of design, technology, and creativity we are taking <em>Sketchbook Sessions</em> national with partners like <a href='http://ga.co/' target='_blank'>General Assembly</a>, <a href='https://hootsuite.com/' target='_blank'>HootSuite</a>, and more to be announced. Furthermore, sponsorship of these events will fit our <a href='http://sketchbook.nclud.com/byte-back/'>model of giving back directly to the community</a>.</p>

	<p>In the meantime, I’d like to invite you to the first installment in this series tonight, on the <a href='http://www.meetup.com/Data-Visualization-DC/events/167470262/' target='_blank'>Art &amp; Code of Data Visualization</a> with <a href='http://alignedleft.com/' target='_blank'>Scott Murray</a> co-presented by <a href='http://www.meetup.com/Data-Visualization-DC/' target='_blank'>Data Visualization DC</a>. We’re really excited to present this platform that goes behind the scenes of our company, community, &amp; industry to create an open &amp; creative discourse.</p>


